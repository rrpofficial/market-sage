# ── Nifty 500 (= all four sub-indices, deduplicated) ─────────────────────────
def _dedup(lists: list[list[str]]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for lst in lists:
        for sym in lst:
            if sym not in seen:
                seen.add(sym)
                result.append(sym)
    return result


